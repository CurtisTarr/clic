package maths

// Add function adds x and y
func Add(x float64, y float64) float64 {
	return x + y
}

// Subtract function subtracts x and y
func Subtract(x float64, y float64) float64 {
	return x - y
}

// Divide function divides x and y
func Divide(x float64, y float64) float64 {
	return x / y
}

// Multiply function multiplies x and y
func Multiply(x float64, y float64) float64 {
	return x * y
}
